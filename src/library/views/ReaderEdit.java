    private void btnConfirmChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmChangesActionPerformed
        String newFirstName = txtFieldNewFirstName.getText().trim();
        String newLastName = txtFieldNewLastName.getText().trim();
        String newAddress = txtFieldNewAddress.getText().trim();
        if(newFirstName.isEmpty()){
            newFirstName = reader.getFirstName();
        }
        if(newLastName.isEmpty()){
            newLastName = reader.getLastName();
        }
        if(newAddress.isEmpty()){
            newAddress = reader.getAddress();
        }
        
        int n = JOptionPane.showConfirmDialog(this,"Confirm Update \n\n First Name: "+ newFirstName+" \n "+
                "Last Name: "+newLastName+
                "\nAddress: "+newAddress,
                "\nConfirm Update",
                JOptionPane.YES_NO_OPTION);
        if(n==0){
            try {
                new ReaderController().editReaderInfo(reader,newFirstName,newLastName,newAddress);
            } catch (InvalidFileException | IOException ex) {
                Logger.getLogger(ReaderEdit.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Reader Updated");
            this.mf.showReaderSearch();
        }
    }//GEN-LAST:event_btnConfirmChangesActionPerformed


